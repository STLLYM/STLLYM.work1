#在刚开始写的时候以为是要写那种不重复位数的奇数种类，然后没写出来，然后看到
#网上解释这个题是可重复的不限位数的奇数总数
#一位时为1，3，5，7四个
#二位时为7*4首位不为0
#三位时为7*8*4
#以此类推
x=int(input("到多少位的奇数："))
s,sum=0,0
for i in range(1,x+1):
     if i ==1:
        s = 4
     elif i ==2:
        s =4*7
     if i>2:
        s *=8

     sum+=s
     print( '%d位数的奇数个数为%d'%(i,s))
     print ('总和为：sum=%d'%sum)

